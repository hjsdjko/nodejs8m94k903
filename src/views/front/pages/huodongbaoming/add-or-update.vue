			async onSubmitTap() {
//跨表计算判断
				var obj;
				obj = uni.getStorageSync('crossObj');
				var table = uni.getStorageSync('crossTable');
				//obj.kebaorenshu = obj.kebaorenshu - this.ruleForm.kebaorenshu
